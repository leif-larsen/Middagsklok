import { writeFile } from "node:fs/promises";
import path from "node:path";

const baseUrl = process.argv[2] ?? process.env.API_BASE_URL ?? "";

if (!baseUrl) {
  console.error("Missing API base URL. Pass as argument or set API_BASE_URL.");
  process.exit(1);
}

const response = await fetch(`${baseUrl}/ingredients/metadata`);

if (!response.ok) {
  console.error(`Failed to fetch metadata: ${response.status} ${response.statusText}`);
  process.exit(1);
}

const data = await response.json();
const categories = Array.isArray(data?.categories) ? data.categories : [];
const units = Array.isArray(data?.units) ? data.units : [];

const toUnion = (values) =>
  values.map((value) => `  | "${value}"`).join("\n");

const categoryValues = categories.map((category) => category.value);
const unitValues = units.map((unit) => unit.value);

const content = `/* eslint-disable */\n// This file is generated by scripts/generate-ingredient-models.mjs\n\nexport type IngredientCategoryValue =\n${toUnion(categoryValues)};\n\nexport type IngredientUnitValue =\n${toUnion(unitValues)};\n`;

const outputPath = path.join(
  process.cwd(),
  "src/Frontend/middagsklok/app/lib/models/ingredients.ts",
);

await writeFile(outputPath, content, "utf8");
console.log(`Generated ${outputPath}`);
